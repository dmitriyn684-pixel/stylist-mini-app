/**
 * Не ML-прогноз, а простая база знаний по составу ткани — честная эвристика
 * по ключевым словам, а не персональное предсказание износа конкретной вещи.
 */
export function fabricCareTip(fabric: string): string {
  const f = fabric.toLowerCase();
  if (!f.trim()) {
    return 'Укажи состав ткани — подскажем, как ухаживать и чего ждать при стирке.';
  }
  if (f.includes('хлопок') || f.includes('лён') || f.includes('лен')) {
    return 'После 20 стирок при 30°C: может дать усадку 2-3%, цвет останется стабильным. Не отжимай на высоких оборотах.';
  }
  if (f.includes('шёлк') || f.includes('шелк')) {
    return 'Деликатная ткань — только ручная стирка или режим для шёлка при 30°C. Не выкручивать, сушить в тени.';
  }
  if (f.includes('шерст')) {
    return 'Возможна усадка и скатывание (пиллинг) при машинной стирке. Стирай вручную холодной водой, сушить горизонтально.';
  }
  if (f.includes('полиэстер') || f.includes('акрил')) {
    return 'Синтетика устойчива к усадке, но со временем может накапливать статику и терять яркость цвета от частой стирки.';
  }
  if (f.includes('деним') || f.includes('джинс')) {
    return 'Первые стирки могут дать усадку до 3-5% и лёгкую потерю цвета. Стирай вывернутым наизнанку в холодной воде.';
  }
  if (f.includes('кож')) {
    return 'Не стирать в машине — только специальные средства для кожи. Избегай прямого солнца и влаги.';
  }
  return 'Точных данных по этому составу нет — ориентируйся на ярлык производителя и стирай при щадящей температуре.';
}
